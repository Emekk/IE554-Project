import numpy as np


np.random.seed(42)
random_n = 20
max_degree = random_n // 5
min_degree = 1
random_graph = np.zeros((random_n, random_n))
for i in range(random_n):
    degree = np.random.randint(min_degree, max_degree + 1)
    neighbors = np.random.choice(random_n, degree, replace=False)
    for j in neighbors:
        if i != j:
            random_graph[i][j] = 1
            random_graph[j][i] = 1

